    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        String id =jTextId.getText();
        try {
            postg=PostgraduateController.searchPostgraduate(id);
            if(postg==null){
                JOptionPane.showMessageDialog(adminPanel, "Not a Registered Student");
                UpdatePostgraduate updatePostg=new UpdatePostgraduate(adminPanel);
                adminPanel.masterPanel.removeAll();
                adminPanel.masterPanel.add(updatePostg);
                adminPanel.masterPanel.repaint();
                adminPanel.masterPanel.revalidate();
                updatePostg.setVisible(true);
            }
            jTextName.setText(postg.getName());
            jTextAddress.setText(postg.getAddress());
            jTextContact.setText(postg.getContact());
            jTextEmail.setText(postg.getEmail());
            String dateValue = postg.getDoB();
            SimpleDateFormat fmt = new SimpleDateFormat("yyyy-MM-dd");
            Date date = fmt.parse(dateValue);
            jPickerDoB.setFormats(fmt);
            jPickerDoB.setDate(date);
            String year,sem;
            String all=postg.getIntake();
            year=all.substring(0,4);
            sem=all.substring(4,all.length());
            if(sem.equals("S1")){
                jTextIntakeSem.setText("Semester 1");
            }else{
                jTextIntakeSem.setText("Semester 2");
            }
            jTextIntakeYear.setText(year);
            jTextCompletion.setText(postg.getCompletionYear());
            jTextQualification.setText(postg.getQualificationType());
            String[] Fid={"FCS01","FBS02","FES03"};
            String[] Faculty={"Computer Science","Bussiness","Engineering"};
            int j=0;
            for(int i=0;i<Fid.length;i++){
                if(Fid[i].equals(postg.getFaculty())){
                    jTextFaculty.setText(Faculty[j]);
                    i=i+1;
                    j=j+1;
                    break;
                }
            }


        } catch (ClassNotFoundException | SQLException | ParseException ex) {
            ExceptionHandle.showError(ex);
        }
    }//GEN-LAST:event_jButtonSubmitActionPerformed

